for tc in range(1, 1+int(input())):
    s = input()

    if s == 'SUN':
        print(f'#{tc} 7')
    elif s == 'MON':
        print(f'#{tc} 6')
    elif s == 'TUE':
        print(f'#{tc} 5')
    elif s == 'WED':
        print(f'#{tc} 4')
    elif s == 'THU':
        print(f'#{tc} 3')
    elif s == 'FRI':
        print(f'#{tc} 2')
    elif s == 'SAT':
        print(f'#{tc} 1')
